function findSkillest(list) {
  const entries = Object.entries(list);
  let mostSkills = entries[0][1].skills.length;
  let person = entries[0][0];

  for (let x = 0; x < entries.length; x++) {
    let actualPerson = entries[x][0];
    let skillSum = entries[x][1].skills.length;
    if (skillSum > mostSkills) {
      mostSkills = skillSum;
      person = actualPerson;
    }
  }

  return `Skillest: ${person} - Total Skills: ${mostSkills}`;
}

