let firstName = "Jhony";
let lastName = "Rodrigues";
let fnameLength = firstName.length;
let lnameLength = lastName.length;

fnameLength > lnameLength
  ? console.log(
      `Your first name ${firstName} is longer than your last name ${lastName}.`
    )
  : console.log(
      `Your first name ${firstName} is shorter than your last name ${lastName}.`
    );
